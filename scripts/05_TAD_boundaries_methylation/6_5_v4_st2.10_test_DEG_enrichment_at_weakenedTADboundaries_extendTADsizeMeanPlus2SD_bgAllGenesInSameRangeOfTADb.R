### (1) Load diffIS TADb and calculate mean TAD size + 2SD

## Load diffIS results
# generated by 6_5_differential_ISnonsegdup_analysis_v4_afterGRC_TADbOnly.R
lnames = load("p_magnitude_Bulk_lmeDiagnosisAgeSexBatch2.TemporalABNNICHDvalid.interactioncis.longRange_includingTechRepsAndOutliers.rda") # p, magnitude, diffIS 
rm(p, magnitude)

## Calculate mean and SD of TAD size 
df = as.data.frame(str_split_fixed(rownames(diffIS), "_", 3))
df$V2 = as.integer(df$V2); df$V3 = as.integer(df$V3)
colnames(df) = c("chr", "start", "end")
diffIS = cbind(df, diffIS); rm(df)

TADsize = diffIS$start[-1] - diffIS$end[-nrow(diffIS)]
TADsize = TADsize[TADsize > 0]
hist(TADsize, xlim = c(0,2000000), breaks = 1000)
M = mean(TADsize) # 400kb
SD = sd(TADsize) # 605kb
W = (M + 2 * SD)/1000 # 1.6M (W has a unit of kb)

### (2) Load and plot Fisher's Exact Test on DEG enrichment at different distance to weakened TADb 

rm(list = setdiff(ls(), "W"))
IS_FDR_threshold = 0.05
lnames = load("6_5_v4_st2.7_DEGenrichment_atWeakenedTADbExtUpTo5Mb_wISfdr0.05_FisherRes_bgAllGenesInSameRangeOfAllTADb.rda") # Fisher_df
