## Background: Luppino 2020 Nat Genet states that "We found a significant enrichment of DEGs within 2.5 and 5.0 kb of a domain boundary (Fig. 5f)." after cohesin loss, and that "most DEGs with > 2-fold change in expression following cohesin loss were located within 50kb of a domain boundary". And CTCF had opposite effect on TAD boundary strength compared to cohesin loss.

## Goal in this script, I want to perform Fisher's exact test on DEG enrichment at TAD boundaries that became significantly weaker in ASD, using all genes in the same range of all TAD boundaries as background: 
#       TSS within x kb (eg. 50kb) of TAD boundary
#               Y              N
#       ------------------------------
# DEG Y |               |             |
#       ------------------------------
# DEG N |               |             |
#       ------------------------------

## After seeing 6_5_v4_st2.7_DEGenrichment_atWeakenedTADbExt0To15Mb_UnequalStep_wISfdr0.05_FisherRes_bgAllGenesInSameRangeOfAllTADb.pdf generated by 6_5_v4_st2.7_test_DEG_enrichment_at_weakenedTADboundaries_extend5Mb_bgAllGenesInSameRangeOfTADb.R, Dan suggested to use a window of TAD mean size + 2SD, which shall give a clear view of the drop of enrichment OR of DEGs with increasing distance from weakened TAD boundaries.

##### pipeline #####
### (1) Load diffIS TADb and calculate mean TAD size + 2SD
### (2) Load and plot Fisher's Exact Test on SFARI gene enrichment at different distance to weakened TADb 
# * calculations done in 6_5_v4_st2.7_test_DEG_enrichment_at_weakenedTADboundaries_extend5Mb_bgAllGenesInSameRangeOfTADb.R
### (3) What proportion of DEGs are located within 1.5Mb (mean TAD size + 2SD) of weakened TADb
####################

